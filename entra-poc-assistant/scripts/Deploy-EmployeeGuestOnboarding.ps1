# Step 10: Create Sign-in Risk Conditional Access Policy
# ============================================================================
#region Step10
$currentStep++
Write-Host "`n[$currentStep/$totalSteps] Creating sign-in risk Conditional Access policy..." -ForegroundColor Cyan

$caPolicyName = "POC-SignInRisk-RequireMFA"
if ($pilotGroupId -and $breakGlassGroupId) {
    try {
        $existingCA = Invoke-MgGraphRequest -Method GET `
            -Uri "https://graph.microsoft.com/v1.0/identity/conditionalAccess/policies?`$filter=displayName eq '$caPolicyName'" `
            -ErrorAction Stop

        if ($existingCA.value.Count -gt 0) {
            Write-Host "  CA policy '$caPolicyName' already exists (id: $($existingCA.value[0].id)), skipping." -ForegroundColor Yellow
        } else {
            if ($PSCmdlet.ShouldProcess($caPolicyName, "Create Conditional Access policy (report-only mode)")) {
                $caBody = @{
                    displayName = $caPolicyName
                    state       = "enabledForReportingButNotEnforced"
                    conditions  = @{
                        users = @{
                            includeGroups = @($pilotGroupId)
                            excludeGroups = @($breakGlassGroupId)
                        }
                        applications = @{
                            includeApplications = @("All")
                        }
                        signInRiskLevels = @("high", "medium")
                    }
                    grantControls = @{
                        operator        = "OR"
                        builtInControls = @("mfa")
                    }
                    sessionControls = @{
                        signInFrequency = @{
                            value     = 1
                            type      = "hours"
                            isEnabled = $true
                            authenticationType = "primaryAndSecondaryAuthentication"
                            frequencyInterval  = "everyTime"
                        }
                    }
                } | ConvertTo-Json -Depth 10

                $newCA = Invoke-MgGraphRequest -Method POST `
                    -Uri "https://graph.microsoft.com/v1.0/identity/conditionalAccess/policies" `
                    -Body $caBody -ContentType "application/json"
                Write-Host "  Created CA policy: $caPolicyName (id: $($newCA.id))" -ForegroundColor Green
                Write-Host "  Policy state: REPORT-ONLY (review sign-in logs before enabling)" -ForegroundColor Yellow
            }
        }
    } catch {
        Write-Host "  ERROR: Failed to create CA policy: $_" -ForegroundColor Red
        Write-Host "  TIP: Verify Conditional Access Administrator role and P2 license." -ForegroundColor Yellow
    }
} else {
    Write-Host "  SKIPPED: Pilot group or break-glass group not available. Create groups first." -ForegroundColor Yellow
}
